public class Aritmetica {
	
	/**
	 * Suma 2 números
	 * @param primerSumando
	 * @param segundoSumando
	 * @return la suma de los 2 sumandos
	 */
	public float suma(float primerSumando , float segundoSumando){
		return primerSumando + segundoSumando;
	}
	
	/**
	 * Resta 2 números
	 * @param minuendo
	 * @param sustraendo
	 * @return la resta de los números
	 */
	public float resta(float minuendo , float sustraendo){
		return minuendo - sustraendo;
	}
	
	/**
	 * Multiplica 2 números
	 * @param primerFactor
	 * @param segundoFactor
	 * @return la multiplicacion de los números
	 */
	public float multiplicacion(float primerFactor, float segundoFactor){
		return primerFactor * segundoFactor;
	}
	
	/**
	 * Divide 2 números
	 * @param dividendo
	 * @param divisor
	 * @return la division de los 2 números
	 */
	public float division(float dividendo, float divisor){
		return dividendo / divisor;
	}
}
